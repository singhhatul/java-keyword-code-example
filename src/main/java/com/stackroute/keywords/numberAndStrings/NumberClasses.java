package com.stackroute.keywords.numberAndStrings;

public class NumberClasses {
    public static void main(String[] args) {
        Integer i = -123456;
        Float f = 435f;
        Double d = 65868.685;
        //Convert number value into byte type
        System.out.println("Integer Number as Byte: "+i.byteValue());
        //Print the value as float
        System.out.println("The Float Value is: "+i.floatValue());
        //Convert number value into double type
        System.out.println("Double Value is: " + i.doubleValue());
        //Returns the value of this Number as an Int
        System.out.println(" int Value is: " + f.intValue());
        //Return the value of this Number as a long
        System.out.println("Output Number as long: " + d.longValue());
        //Returns the value of this Number as a short type
        System.out.println("short Value = " + d.shortValue());



    }
}